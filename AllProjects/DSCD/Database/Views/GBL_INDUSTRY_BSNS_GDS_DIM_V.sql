CREATE OR REPLACE VIEW SCD.GBL_INDUSTRY_BSNS_GDS_DIM_V AS
SELECT
GIBDDL_IND_BSNS_CODE IBC,
B.GIB_SHORT_NAME IBC_SHORT_NAME,
GIBDDL_DD1_IND_BSNS_CODE DD1_IBC,
B1.GIB_SHORT_NAME DD1_IBC_SHORT_NAME,
GIBDDL_DD2_IND_BSNS_CODE DD2_IBC,
B2.GIB_SHORT_NAME DD2_IBC_SHORT_NAME,
GIBDDL_DD3_IND_BSNS_CODE DD3_IBC,
B3.GIB_SHORT_NAME DD3_IBC_SHORT_NAME
FROM GBL_IND_BSNS_DRILL_DOWN_LAYERS A,
GBL_INDUSTRY_BUSINESS B,
GBL_INDUSTRY_BUSINESS B1,
GBL_INDUSTRY_BUSINESS B2,
GBL_INDUSTRY_BUSINESS B3
WHERE A.GIBDDL_IND_BSNS_CODE=B.GIB_INDUSTRY_BUSINESS_CODE
AND A.GIBDDL_DD1_IND_BSNS_CODE=B1.GIB_INDUSTRY_BUSINESS_CODE
AND A.GIBDDL_DD2_IND_BSNS_CODE=B2.GIB_INDUSTRY_BUSINESS_CODE
AND A.GIBDDL_DD3_IND_BSNS_CODE=B3.GIB_INDUSTRY_BUSINESS_CODE
;
